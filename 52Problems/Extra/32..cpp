#include<iostream>
using namespace std;

int main()
{
    int a, b;
    int tcase;
    cin >> tcase;
    while ( tcase-- )
    {
        cin >> a >> b;
        if ( a > b )
        {
            cout << "Invalid!" << "\n";
        }
        else
        {
            for ( int i = 1 ;; i++ )
            {
                if ( a * i > b ) break;
                    cout << a*i << "\n";
            }
        }
    }
    return 0;
}
