#include<bits/stdc++.h>
using namespace std;
int main()
{
    freopen ( "2d.txt", "r", stdin );
    int namta[4][10];
    for ( int i = 0 ; i < 4  ; i++ )
    {
        for ( int j = 0 ; j < 10  ; j++ )
        {
            scanf ( "%d", &namta[i][j] );
           // printf ( "%d ", namta[i][j] );
            namta[i][j] = ( i + 1 ) * ( j + 1 ) ;
        }
        cout << endl;
    }

    for ( int i = 0 ; i < 4  ; i++ )
    {
        for ( int j = 0 ; j < 10  ; j++ )
        {
            printf("%d x %d= %d\n", (i+1), (j+1) , namta[i][j]);
        }
        cout<<endl;
    }
    return 0;
}

